package userPack;

public class Comment {
    private String commentId;
    private String postId;
    private String commenterEmail;
    private String commenterType;
    private String commentText;
    private String commentTime;

    // Getter and Setter methods for commentId
    public String getCommentId() {
        return commentId;
    }
    public void setCommentId(String commentId) {
        this.commentId = commentId;
    }

    // Getter and Setter methods for postId
    public String getPostId() {
        return postId;
    }
    public void setPostId(String postId) {
        this.postId = postId;
    }

    // Getter and Setter methods for commenterEmail
    public String getCommenterEmail() {
        return commenterEmail;
    }
    public void setCommenterEmail(String commenterEmail) {
        this.commenterEmail = commenterEmail;
    }

    // Getter and Setter methods for commenterType
    public String getCommenterType() {
        return commenterType;
    }
    public void setCommenterType(String commenterType) {
        this.commenterType = commenterType;
    }

    // Getter and Setter methods for commentText
    public String getCommentText() {
        return commentText;
    }
    public void setCommentText(String commentText) {
        this.commentText = commentText;
    }

    // Getter and Setter methods for commentTime
    public String getCommentTime() {
        return commentTime;
    }
    public void setCommentTime(String commentTime) {
        this.commentTime = commentTime;
    }
}

